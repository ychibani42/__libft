#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;
	int	j;

	i = 0;
	j = ft_strlen(s);
	while (i <= j)
	{
		if ((unsigned char)s[j - i] == (unsigned char)c)
			return ((char *)s + (j - i));
		i++;
	}
	return (0);
}
